class LinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def isEmpty(self):
        return self.head == None

    def add(self, item):
        temp = Node(item)
        temp.setNext(self.head)
        self.head = temp
        if self.tail == None:
            self.tail = temp

    def append(self, item):
        temp = Node(item)
        if self.tail != None:
            self.tail.setNext(temp)
        self.tail = temp
        if self.head == None:
            self.head = temp

    def size(self):
        current = self.head
        count = 0
        while current != None:
            count = count + 1
            current = current.getNext()

        return count

    def printList(self):
        current = self.head
        while current != None:
            print(current.getData(), end=" ")
            current = current.getNext()
        print("")

    def get_head(self):
        return self.head

    def remove_head(self):
        current = self.head.getNext()
        self.head = current
